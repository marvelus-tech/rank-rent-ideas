function run(db, sql, params = []) {
  return new Promise((resolve, reject) => {
    db.run(sql, params, function onRun(err) {
      if (err) reject(err);
      else resolve(this);
    });
  });
}

function get(db, sql, params = []) {
  return new Promise((resolve, reject) => {
    db.get(sql, params, (err, row) => (err ? reject(err) : resolve(row)));
  });
}

async function seedDatabase(db) {
  await new Promise((resolve, reject) => db.exec(SCHEMA, (err) => (err ? reject(err) : resolve())));

  const vehicleCount = await get(db, 'SELECT COUNT(*) AS count FROM vehicles');
  if (!vehicleCount || vehicleCount.count === 0) {
    for (const v of REALISTIC_FLEET_DATA.vehicles) {
      await run(
        db,
        `INSERT INTO vehicles
        (vehicle_id, vin, license_plate, make, model, year, category, daily_rate, mileage, fuel_type, color, status)
        VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)`,
        [v.vehicle_id, v.vin, v.license_plate, v.make, v.model, v.year, v.category, v.daily_rate, v.mileage, v.fuel_type, v.color, 'Available']
      );
    }
  }

  const rentalCount = await get(db, 'SELECT COUNT(*) AS count FROM rentals');
  if (!rentalCount || rentalCount.count === 0) {
    for (const r of REALISTIC_FLEET_DATA.rentals) {
      await run(
        db,
        `INSERT INTO rentals
        (booking_id, customer_name, email, phone, driver_license, vehicle_id, pickup_location, dropoff_location,
         pickup_date, dropoff_date, mileage_out, mileage_in, fuel_level_out, fuel_level_in, insurance_type,
         payment_status, status, total_cost, notes)
         VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)`,
        [r.booking_id, r.customer_name, r.email, r.phone, r.driver_license, r.vehicle_id, r.pickup_location, r.dropoff_location,
         r.start_date, r.end_date, r.mileage_out, r.mileage_in, r.fuel_level_out, r.fuel_level_in, r.insurance_type,
         r.payment_status, r.status, r.total_cost, r.notes]
      );
    }
  }

  return {
    vehiclesInserted: vehicleCount.count === 0 ? REALISTIC_FLEET_DATA.vehicles.length : 0,
    rentalsInserted: rentalCount.count === 0 ? REALISTIC_FLEET_DATA.rentals.length : 0
  };
}

